/**
 * @file test-runner-detector.ts
 * @description Detects the project's test command using a priority chain.
 *
 * Resolution order:
 *   1. `.agencee/config/intelligence/tech-registry.json` → `testRunner` field
 *   2. `package.json` → `scripts.test`
 *   3. Heuristic defaults based on detected package manager / lockfiles
 *   4. Ultimate fallback: `pnpm test`
 *
 * Returns a single command string ready to pass to `toolRunTerminal`.
 */

import * as fs from 'fs/promises';
import * as path from 'path';

// ─── Types (minimal — only the fields we need) ────────────────────────────────

interface TechRegistry {
  testRunner?: string;
  packageManager?: string;
}

interface PackageJson {
  scripts?: Record<string, string>;
}

// ─── Public API ───────────────────────────────────────────────────────────────

/**
 * Detects the appropriate test command for the given workspace.
 * Never throws — falls back to `'pnpm test'` on any error.
 */
export async function detectTestCommand(workspaceRoot: string): Promise<string> {
  // 1. Tech registry (generated by /analyse)
  try {
    const registryPath = path.join(
      workspaceRoot,
      '.agencee',
      'config',
      'intelligence',
      'tech-registry.json',
    );
    const raw = await fs.readFile(registryPath, 'utf-8');
    const registry = JSON.parse(raw) as TechRegistry;
    if (registry.testRunner && typeof registry.testRunner === 'string') {
      return registry.testRunner;
    }
  } catch {
    // Not generated yet — continue
  }

  // 2. package.json scripts.test
  try {
    const pkgPath = path.join(workspaceRoot, 'package.json');
    const raw = await fs.readFile(pkgPath, 'utf-8');
    const pkg = JSON.parse(raw) as PackageJson;
    const testScript = pkg.scripts?.['test'];
    if (testScript && typeof testScript === 'string') {
      // Convert bare script body to a manager-prefixed run command
      return resolveTestScript(testScript, workspaceRoot);
    }
  } catch {
    // No package.json — continue
  }

  // 3. Heuristic based on lockfile presence
  return detectFromLockfile(workspaceRoot);
}

// ─── Internal helpers ─────────────────────────────────────────────────────────

/**
 * If the `scripts.test` value is already a full command (e.g. "jest --ci"),
 * wrap it with the correct package manager. If it already contains "npm" /
 * "pnpm" / "yarn" / "bun" it goes through unchanged.
 */
async function resolveTestScript(script: string, workspaceRoot: string): Promise<string> {
  // Already a full invocation
  if (/^(npm|pnpm|yarn|bun|node|npx)\s/.test(script)) return script;

  const pm = await detectPackageManager(workspaceRoot);
  return `${pm} test`;
}

/**
 * Detects the package manager by checking for lockfiles.
 */
async function detectPackageManager(workspaceRoot: string): Promise<string> {
  const checks: Array<[string, string]> = [
    ['pnpm-lock.yaml', 'pnpm'],
    ['yarn.lock', 'yarn'],
    ['bun.lockb', 'bun'],
    ['bun.lock', 'bun'],
    ['package-lock.json', 'npm'],
  ];

  for (const [lockfile, manager] of checks) {
    try {
      await fs.access(path.join(workspaceRoot, lockfile));
      return manager;
    } catch {
      // Not found
    }
  }

  return 'pnpm';
}

async function detectFromLockfile(workspaceRoot: string): Promise<string> {
  const pm = await detectPackageManager(workspaceRoot);
  return `${pm} test`;
}
